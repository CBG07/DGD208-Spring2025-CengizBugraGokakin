/// <summary>
/// Represents the type of an item (e.g., Food or Toy) in the pet simulation game.
/// </summary>
public enum ItemType
{
    /// <summary>
    /// Food item type, used to feed pets and increase their hunger stat.
    /// </summary>
    Food,
    /// <summary>
    /// Toy item type, used to entertain pets and increase their fun or sleep stat.
    /// </summary>
    Toy
}