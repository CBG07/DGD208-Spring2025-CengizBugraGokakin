/// <summary>
/// Represents the different stats a pet can have in the simulation.
/// </summary>
public enum PetStat
{
    /// <summary>
    /// How hungry the pet is (0 = starving, 100 = full).
    /// </summary>
    Hunger,
    /// <summary>
    /// How sleepy the pet is (0 = exhausted, 100 = fully rested).
    /// </summary>
    Sleep,
    /// <summary>
    /// How much fun the pet is having (0 = bored, 100 = very happy).
    /// </summary>
    Fun
}
}